"""
Operadores de comparação (relacionais)
OP      Significado         Exemplo (True)
>       maior               2 > 1
>=      maior ou igual      2 >= 2
<       menor               1 < 2
<=      menor ou igual      2 <= 2
==      igual               'a' == 'a'
!=      diferente           'a' != 'b'
"""

maior = 5 > 4
maior_ou_igual = 4 >= 4
menor = 2 < 4
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'c'

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)